// DOM (Document Object Model) allows developers to change the structure and content of a web page dynamically using JavaScript. For example, you can use the DOM to add new elements, change the text of existing elements, or respond to user events like clicks and keyboard input.

// It is a programming interface for web documents, and it represents the page so that programs can change the document structure, style, and content. The DOM provides a structured representation of the document as a tree of objects, and it defines methods that allow developers to manipulate these objects. This makes it possible to create interactive and dynamic web pages.

// Example of using DOM to change the title of a web page:

document.title = "Playing with the DOM"; // This will change the title of the web page to "Playing with the DOM"

// To change the background color of the body element:

document.body.style.backgroundColor = "blue"; // This will change the background color of the body to light blue

