// IIFE:

// Immediately Invoke Function Expression

// This is used to automatically exxecute the function rather than calling it.

// You do not need to give name to your function


//1- In simple function:

(function (){
  console.log("Hello")
})();


//2- In Arrow Function:

(()=>{
  console.log("Hello my name is A")
})();


//3- In async Function:

(async function () {

  console.log("Hello i m hehe")
  
})();

